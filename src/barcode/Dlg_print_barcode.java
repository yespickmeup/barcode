    public static class Tblinventory_barcodesModel2 extends AbstractTableAdapter {

        public static String[] COLUMNS = {
            "Qty", "Barcode", "", "Count", ""
        };

        public Tblinventory_barcodesModel2(ListModel listmodel) {
            super(listmodel, COLUMNS);
        }

        @Override
        public boolean isCellEditable(int row, int column) {

            return false;
        }

        @Override
        public Class getColumnClass(int col) {
            if (col == 4) {
                return Boolean.class;
            }
            return Object.class;
        }

        @Override
        public Object getValueAt(int row, int col) {
            Srpt_print_barcodes.field tt = (Srpt_print_barcodes.field) getRow(row);
            switch (col) {
                case 0:
                    return " " + FitIn.fmt_woc(tt.count);
                case 1:
                    return " " + tt.barcode;
                case 2:
                    return "/barcode/clear-button.png";
                case 3:
                    return " " + FitIn.fmt_woc(tt.count);
                default:
                    return tt.selected;
            }
        }
    }

    private void select_item2() {
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            return;
        }

        int col = jTable1.getSelectedColumn();
        if (col == 4) {
            Srpt_print_barcodes.field to = (Srpt_print_barcodes.field) tbl_inventory_barcodes_ALM2.get(row);
            if (to.selected == false) {
                to.setSelected(true);
            } else {
                to.setSelected(false);
            }

        }
        tbl_inventory_barcodes_M2.fireTableDataChanged();

    }

    private void delete_all() {
        Window p = (Window) this;
        Dlg_confirm_action nd = Dlg_confirm_action.create(p, true);
        nd.setTitle("");

        nd.setCallback(new Dlg_confirm_action.Callback() {

            @Override
            public void ok(CloseDialog closeDialog, Dlg_confirm_action.OutputData data) {
                closeDialog.ok();
                tbl_inventory_barcodes_ALM2.clear();
                tbl_inventory_barcodes_M2.fireTableDataChanged();
                jTextField1.grabFocus();
            }
        });
        nd.setLocationRelativeTo(this);
        nd.setVisible(true);

    }

    private void delete_barcode() {
        final int row = jTable1.getSelectedRow();
        if (row < 0) {
            return;
        }
        int col = jTable1.getSelectedColumn();
        if (col == 2) {
            Window p = (Window) this;
            Dlg_confirm_action nd = Dlg_confirm_action.create(p, true);
            nd.setTitle("");

            nd.setCallback(new Dlg_confirm_action.Callback() {

                @Override
                public void ok(CloseDialog closeDialog, Dlg_confirm_action.OutputData data) {
                    closeDialog.ok();
                    tbl_inventory_barcodes_ALM2.remove(row);
                    tbl_inventory_barcodes_M2.fireTableDataChanged();
                }
            });
            nd.setLocationRelativeTo(jScrollPane2);
            nd.setVisible(true);
        }

    }
